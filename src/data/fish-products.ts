/**
 * Get all fish products
 */
export const getAllFishProducts = (): FishProduct[] => fishProducts;

/**
 * Get fish products by category
 */
export const getFishProductsByCategory = (
  category: 'Freshwater' | 'Saltwater' | 'Specialty'
): FishProduct[] => fishProducts.filter((product) => product.category === category);

/**
 * Get a single fish product by ID
 */
export const getFishProductById = (id: string): FishProduct | undefined =>
  fishProducts.find((product) => product.id === id);

/**
 * Search fish products by name or description
 */
export const searchFishProducts = (query: string): FishProduct[] => {
  const lowerQuery = query.toLowerCase();
  return fishProducts.filter(
    (product) =>
      product.name.toLowerCase().includes(lowerQuery) ||
      product.scientificName.toLowerCase().includes(lowerQuery) ||
      product.description.toLowerCase().includes(lowerQuery)
  );
};
